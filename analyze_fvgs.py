# Session classifications (ADL time)
def classify_session(hour):
    """Classify hour into session"""
    if 10 <= hour < 14:
        return "Morning (10-2)"
    elif 14 <= hour < 18:
        return "Early PM (2-6)"
    elif 18 <= hour < 22:
        return "Evening (6-10)"
    else:
        return "Night (10-2)"

